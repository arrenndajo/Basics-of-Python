# While Loop

# Print 1 to 5
i = 1
while i <= 5:
    print(i)
    i = i + 1

# Placement Questions:
# Stars 1 time to 5 times
i = 1
while i <= 5:
    print(i * '* ')
    i = i + 1

# Print Hello 1 time to 5 times
i = 1
while i <= 5:
    print(i * 'hello ')
    i = i + 1

# Stars upside down 5 times to 1 time
i = 5
while i > 0:
    print(i * '* ')
    i = i - 1

